package com.example.quickweather.util;

public class Region {
    private String regionName;
    private String regionAdcode;
    private String regionCitycode;

    public void setRegionName(String regionName) {
        this.regionName = regionName;
    }

    public String getRegionName() {
        return regionName;
    }

    public void setRegionAdcode(String regionAdcode) {
        this.regionAdcode = regionAdcode;
    }

    public String getRegionAdcode() {
        return regionAdcode;
    }

    public void setRegionCitycode(String regionCitycode) {
        this.regionCitycode = regionCitycode;
    }

    public String getRegionCitycode() {
        return regionCitycode;
    }
}
